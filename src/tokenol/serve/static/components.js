export function esc(s) {
  return String(s ?? '').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

/** Render a delta chip element for a ratio value (val / baseline). */
export function deltaBadge(ratio, invertGood = false) {
  if (ratio == null) return '';
  const pct = Math.round((ratio - 1) * 100);
  if (pct === 0) return '';
  const sign = pct > 0 ? '↑' : '↓';
  let cls;
  if (invertGood) {
    cls = pct < 0 ? 'delta-down-good' : ratio >= 1.5 ? 'delta-up-red' : 'delta-up-amber';
  } else {
    cls = pct > 0 ? 'delta-down-good' : ratio <= 0.75 ? 'delta-up-red' : 'delta-up-amber';
  }
  return `<span class="delta-chip ${cls}">${sign}${Math.abs(pct)}%</span>`;
}

/** Render a verdict pill span. */
export function verdictPill(v) {
  return `<span class="verdict-pill verdict-${v}">${v.replace(/_/g,' ')}</span>`;
}

/** Render a verdict distribution list. */
export function renderVerdictDist(containerId, dist) {
  const el = document.getElementById(containerId);
  if (!el) return;
  const VERDICTS = [
    ['OK','vd-ok'], ['CONTEXT_CREEP','vd-creep'], ['RUNAWAY_WINDOW','vd-runaway'],
    ['TOOL_ERROR_STORM','vd-tool'], ['SIDECHAIN_HEAVY','vd-sidechain'],
  ];
  const total = Object.values(dist).reduce((s, v) => s + v, 0) || 1;
  // Render every verdict label with a zero-width bar when its count is 0 so
  // the panel stays visually stable across ranges (1D with a single session
  // previously showed a one-row list that looked like a data loading bug).
  el.innerHTML = VERDICTS.map(([key, cls]) => {
    const cnt = dist[key] ?? 0;
    const w   = cnt > 0 ? (cnt / total * 100).toFixed(1) : 0;
    const mute = cnt === 0 ? ' vd-empty' : '';
    return `<div class="vd-row ${cls}${mute}">
      <span class="vd-label">${key.replace(/_/g,' ')}</span>
      <div class="vd-bar-track"><div class="vd-bar-fill" style="width:${w}%"></div></div>
      <span class="vd-count">${cnt}</span>
    </div>`;
  }).join('');
}

/** Render a cache-hit% inline bar cell. */
export function cacheHitBar(rate) {
  if (rate == null) return '<td>–</td>';
  const pct = Math.round(rate * 100);
  const cls = rate < 0.25 ? 'bar-red' : rate < 0.50 ? 'bar-amber' : 'bar-good';
  return `<td><div class="inline-bar-wrap">
    <div class="inline-bar-track"><div class="inline-bar-fill ${cls}" style="width:${pct}%"></div></div>
    <span>${pct}%</span>
  </div></td>`;
}

/** Render a max-input-ratio inline bar cell. */
export function maxInputBar(ratio) {
  if (ratio == null) return '<td>–</td>';
  const pct = Math.round(ratio * 100);
  const cls = ratio >= 0.85 ? 'bar-red' : ratio >= 0.70 ? 'bar-amber' : 'bar-good';
  return `<td><div class="inline-bar-wrap">
    <div class="inline-bar-track"><div class="inline-bar-fill ${cls}" style="width:${Math.min(pct,100)}%"></div></div>
    <span>${pct}%</span>
  </div></td>`;
}

/** Render a context growth rate cell. */
export function growthCell(rate) {
  if (!rate) return '<td class="mute">–</td>';
  const k = (rate / 1000).toFixed(1);
  const cls = rate > 5000 ? 'alarm' : rate > 2000 ? 'amber' : 'mute';
  return `<td class="${cls}">${k}k/turn</td>`;
}

/** Standard session rows HTML (shared between main and drill-down pages). */
export function sessionRows(sessions) {
  return sessions.map(s => {
    const isOk = s.verdict === 'OK';
    const rowCls = !isOk ? 'row-flagged-red' : '';
    return `<tr data-id="${s.id}" class="${rowCls}" style="cursor:pointer" title="${s.cwd ?? ''}">
      <td>${s.id.slice(0,8)}</td>
      <td>${shortModel(s.model)}</td>
      <td>${fmtUSD(s.cost_usd)}</td>
      <td>${s.turns}</td>
      ${maxInputBar(s.max_input_ratio)}
      ${growthCell(s.context_growth_rate)}
      ${cacheHitBar(s.cache_hit_rate)}
      <td>${verdictPill(s.verdict)}</td>
    </tr>`;
  }).join('');
}
